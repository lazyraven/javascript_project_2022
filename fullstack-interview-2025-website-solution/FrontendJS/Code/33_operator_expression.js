// Arithmetic Operator
let result = 10 + 5;
console.log(result); // 15 - Here + is an operator, and 10 + 5 is an expression that evaluates to 15

// Comparison Operator
console.log(10 > 5); // true - This expression evaluates to a boolean value.

// Logical Operator
let age = 20;
let hasID = true;

console.log(age > 18 && hasID); // true - Here && combines two expressions into one.

// Assignment Expression
let x = 10; // - This is also an expression because it returns the assigned value.

// Bonus: Ternary Operator - // The ternary operator is a short form of if-else and is also an expression
let age = 18;

let message = age >= 18 ? "Adult" : "Minor";
console.log(message);


// Since expressions return values, they can be nested or used inside other expressions.
let result = (5 + 3) * 2;